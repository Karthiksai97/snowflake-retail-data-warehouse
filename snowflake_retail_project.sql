--- Warehose CREATE

CREATE WAREHOUSE SAMPLE_KARTHIK
WITH 
WAREHOUSE_SIZE = 'XSMALL'
AUTO_SUSPEND = 60
AUTO_RESUME = TRUE
INITIALLY_SUSPENDED = TRUE;

USE WAREHOUSE SAMPLE_KARTHIK;

--Database CREATE

CREATE DATABASE RETAIL_DWH;

use database retail_dwh;

-- schema CREATE


CREATE SCHEMA RAW;
CREATE SCHEMA STAGE;
CREATE SCHEMA ANALYTICS;


--File format 


CREATE FILE FORMAT CSV_FORMAT
TYPE = CSV
FIELD_OPTIONALLY_ENCLOSED_BY='"'
SKIP_HEADER = 1;

-- Stage creation

CREATE STAGE RETAIL_STAGE
FILE_FORMAT = CSV_FORMAT;

LIST @RETAIL_STAGE;



--- RAW TABLE CREATION

CREATE TABLE RAW.SUPERSTORE_RAW (
SHIP_MODE STRING,
SEGMENT STRING,
COUNTRY STRING,
CITY STRING,
STATE STRING,
POSTAL_CODE STRING,
REGION STRING,
CATEGORY STRING,
SUB_CATEGORY STRING,
SALES FLOAT,
QUANTITY INT,
DISCOUNT FLOAT,
PROFIT FLOAT
);


-- Loading data

COPY INTO RAW.SUPERSTORE_RAW
FROM @RETAIL_STAGE/SampleSuperstore.csv;

